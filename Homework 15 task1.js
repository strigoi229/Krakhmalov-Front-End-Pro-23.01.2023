//Напишіть функцію min(a, b), яка повертає менше з двох чисел a та b.
function min(a, b) {
    if (a < b) {
        return console.log(a);
    } else console.log(b);
}
min(5, 100);

//Напишіть функцію pow(x, n), яка повертає число x в степені n
function pow(x, n) {
    return console.log(x ** n);
}
pow(2, 3);



























